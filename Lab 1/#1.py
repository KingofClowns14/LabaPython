# Задание 10 Напишите калькулятор для целочисленных вычислений, который обрабатывает серию 
# команд и выводит результат вычислений по каждой из них.

while True:
    n1 = int(input('Введите первое число: '))
    op = input('Введите операцию (+, -, *, /, %, ! для факториала, x для выхода): ')
    if op == 'x':
        print(n1)
        break
    elif op == '!':
        if n1 >= 0:
            res = 1
            for i in range(1, n1 + 1):
                res *= i
            print(res)
    else:
        n2 = int(input('Введите второе число: '))
        if op == '+':
            print(n1 + n2)
        elif op == '-':
            print(n1 - n2)
        elif op == '*':
            print(n1 * n2)
        elif op == '/':
            if n2 != 0:
                print(n1 // n2)
        elif op == '%':
            if n2 != 0:
                print(n1 % n2)

# Задние 11 Выведите пирамиду из символов «*» заданной высоты.

height = int(input('Введите высоту пирамиды: '))
for i in range(1, height + 1):
    spaces = ' ' * (height - i)
    stars = '*' * (2 * i - 1)
    print(spaces + stars)

# Задание 12 Напишите программу, принимающую на вход целое положительное число N, и выводящую 
# на экран последовательность от 1 до N «ёлочкой». 

N = int(input('Введите целое положительное число N: '))
current_num = 1
row_size = 1
while current_num <= N:
    row = []
    for _ in range(row_size):
        if current_num <= N:
            row.append(str(current_num))
            current_num += 1
    if row:        
        print(' '.join(row))
    row_size += 1

# Задание 13 Напишите программу построения прямоугольника n х m, состоящего из символов symb. 
# Фигура должна быть пустой, а не заполненной. То есть она должна состоять только из 
# контура. 

n = int(input("Введите высоту n: "))
m = int(input("Введите ширину m: "))
symb = input("Введите символ: ")
for i in range(n):
    if i == 0 or i == n - 1:
        print(symb * m)
    else:
        if m > 1:
            print(symb + ' ' * (m - 2) + symb)
        else:
            print(symb)