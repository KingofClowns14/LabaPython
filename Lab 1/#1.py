# Задание 8 Напишите программу, которая считывает рост претендентов в отряд космонавтов до тех 
# пор, пока не будет введен «!». А затем выводит на первой строчке количество подходящих 
# кандидатур, а на второй строке – минимальный и максимальный рост участников, 
# отобранных в новый отряд космонавтов. Рост космонавта не может быть больше 190 см и 
# меньше 150 см.

count = 0
min_height = 191
max_height = 149
while True:
    data = input('Введите рост претендента (или "!" для завершения): ')
    if data == '!':
        break
    height = int(data)
    if 150 <= height <= 190:
        count += 1
        if height < min_height:
            min_height = height
        if height > max_height:
            max_height = height
print(f'количество подходящих кандидатур: {count}')
if count > 0:
    print(f'Минимальный рост: {min_height}, Максимальный рост: {max_height}')

